package gun27._02_Ornek;

public class MyMathClass {

    public static int maxAl(int a, int b) {

        return Math.max(a, b);
        // return a>b ? a : b;



    }

    public static int minAl(int a, int b) {

        return Math.max(a, b);

    }

    public static int random(int a) {
        return (int) (Math.random() * a);

    }

    public static double usAl(int a, int b) {

        return Math.pow(a, b);

    }

    public static double karekokAl(int a) {

        return Math.sqrt(a);

    }

}

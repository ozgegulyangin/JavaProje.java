package gun03;

public class _01_Degiskenler {
    public static void main(String[] args) {
        int sayi; //bana int miktarınca bir tam sayı saklayacağım bir yer ayır adını "sayi" verdim.

        sayi=5; // sayiya 5 değerini verdim.
        System.out.println("sayi"); // sayi kelimesini yazar, tırnak içini dikkate alır
        System.out.println(sayi); // 5 yazar , atadığım değeri yazar

        System.out.print("sayi=");
        System.out.println(sayi); // sayi=5 yazdık

        System.out.println("sayi="+sayi); // sayi= sabit yazısının yanına sayinın değerini yazdı
        // "+" yanına ekle/bitiştir anlamına geliyor

    }
}

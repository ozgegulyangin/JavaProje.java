package gun03;

public class _03_JavaDataTypes {
    public static void main(String[] args) {
        // tam sayılar
        // Default INT
        byte byteDeger = 7;
        short shortDeger = 1645;
        int intDeger = 2500000;
        long longDeger = 37452389462423605L;
        //Ondalık Sayı
        // Default Double'dır
        double doubleDeger = 3.85375674967; // noktadan sonra 16 hane duyarlıdır
        float floatDeger = 3.244763F; // noktadan sonra 7 hane duyarlıdır.
        //Karaktarler
        char basHarf = 'A' ;// bir tane karakter saklanabilir
        String isim = "Özge" ; // kelime veya cümle saklayabilir
        System.out.println("byteDeger = " + byteDeger);
        System.out.println("shortDeger = " + shortDeger);
        System.out.println("intDeger = " + intDeger);
        System.out.println("longDeger = " + longDeger);
        System.out.println("doubleDeger = " + doubleDeger);
        System.out.println("floatDeger = " + floatDeger);
        System.out.println("basHarf = " + basHarf);
        System.out.println("isim = " + isim);




    }
}
